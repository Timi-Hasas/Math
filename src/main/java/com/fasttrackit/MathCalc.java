package com.fasttrackit;

public class MathCalc {

    public float Sum(int x, int y)
    {
        float toReturn = x + y;
        return toReturn;
    }

    public float Divide(int x, int y)
    {
        float toReturn = x / y;
        return toReturn;
    }

    public float Extract(int x, int y)
    {
        float toReturn = x - y;
        return toReturn;
    }

    public float Multiply(int x, int y)
    {
        float toReturn = x * y;
        return toReturn;
    }

}
